//Steve Tointon Loop Practice
public class LoopPractice 
	{

	public static void main(String[] args) 
		{
		for (int i=0 ; i<5 ; i++)
			{
			System.out.println("I love donuts!");
			}
		for (int i=1 ; i<6 ; i++)
			{
			System.out.println(i);
			}
		for (int i=1 ; i<7 ; i++)
			{
			System.out.println(i*(11-1));
			}
		System.out.println("*********");
		for (int i=1 ; i<8 ; i++)
			{
			System.out.println("*       *");
			}
		System.out.println("*********");
		for (int i=5 ; i>0; i--)
			{
			System.out.println(i);
			}
		}
	}
